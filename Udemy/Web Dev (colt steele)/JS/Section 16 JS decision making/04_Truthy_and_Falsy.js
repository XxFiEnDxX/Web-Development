//Every value in JS have some Truthy-ness or Falsy-ness values.

// All JS values have an inherent truthyness or
// falsyness about them

// Falsy values:
// false
// 0
// "" (empty string)
// undefined
// null
// NaN

// Everything else is truthy!

//Can be use in IF ELSE conditions

if ("") {
    console.log("Truthy");
} else {
    console.log("Falsy");   
}

