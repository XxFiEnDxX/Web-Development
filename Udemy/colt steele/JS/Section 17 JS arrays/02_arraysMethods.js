let arr = [1,2,3];

//push & pop

// Push - add to end
// Pop - remove from end


console.log(arr);

arr.push(4,5)
arr.push(6)

console.log(arr);

arr.pop()

console.log(arr);

//shift & unshift

// Shift - remove from start
// Unshift - add to start

arr.shift();
arr.shift();

console.log(arr);

arr.unshift(1,2)

console.log(arr);


